    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (jTable1.getSelectedRow() != -1) {
            String idparametro = (String) amodel.getValueAt(jTable1.getSelectedRow(), 0);
            String descripcion = (String) amodel.getValueAt(jTable1.getSelectedRow(), 1);
            String idtipo = (String) amodel.getValueAt(jTable1.getSelectedRow(), 2);
            String tipo = (String) amodel.getValueAt(jTable1.getSelectedRow(), 3);
            String formula = (String) amodel.getValueAt(jTable1.getSelectedRow(), 4);
            String idtabla = (String) amodel.getValueAt(jTable1.getSelectedRow(), 5);
            String tabla = (String) amodel.getValueAt(jTable1.getSelectedRow(), 6);

            this.lblidparametro.setText(idparametro);
            this.txtdescripcion.setText(descripcion);

            this.cmbtipo.setSelectedItem(idtipo + " " + tipo);

            this.txtformula.setText(formula);

            this.cmbtabla.setSelectedItem(idtabla + " " + tabla);

        } else {
            System.out.println("Seleccione un renglon primero");
        }


    }//GEN-LAST:event_jTable1MouseClicked

