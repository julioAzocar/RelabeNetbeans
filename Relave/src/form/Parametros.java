    private void buguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buguardarActionPerformed
        // TODO add your handling code here:
        if (this.txtdescripcion.getText().trim().equals("") == true) {
            JOptionPane.showMessageDialog(null, "Ingrese descripcion de parametro");
            return;
        }

        String tipo = cmbtipo.getSelectedItem().toString();
        if (tipo.equals("") == true) {
            JOptionPane.showMessageDialog(null, "Ingrese tipo de parametro");
            return;
        } else {
            tipo = tipo.substring(0, tipo.indexOf(" "));
        }

        String tabla = cmbtabla.getSelectedItem().toString();
        if (tabla.equals("") == true) {
            JOptionPane.showMessageDialog(null, "Ingrese tabla de parametro");
            return;

        } else {
            tabla = tabla.substring(0, tabla.indexOf(" "));
        }

        if (this.txtformula.getText().trim().equals("") == true) {
            JOptionPane.showMessageDialog(null, "Ingrese formula de parametro");
            return;
        }

        CParametro objt = new CParametro(Long.parseLong(this.lblidparametro.getText()),
                this.txtdescripcion.getText(),
                Long.parseLong(tipo),
                this.txtformula.getText().trim(),
                Long.parseLong(tabla));

        objt.Guardar();

        if (objt.xError.equals("") == true) {
            Limpiar();
            LlenaTabla();
            JOptionPane.showMessageDialog(null, "Parametro guardado correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error : \n" + objt.xError);
        }
        

    }//GEN-LAST:event_buguardarActionPerformed

    private void bueliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bueliminarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        if (Long.parseLong(this.lblidparametro.getText()) == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione parametro en lista");
            return;
        }

        CParametro objt = new CParametro(Long.parseLong(this.lblidparametro.getText()),
                "",
                0,
                "",
                0);

        objt.Eliminar();

        if (objt.xError.equals("") == true) {
            Limpiar();
            LlenaTabla();
            JOptionPane.showMessageDialog(null, "Parametro eliminado correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error : \n" + objt.xError);
        }
    }//GEN-LAST:event_bueliminarActionPerformed

