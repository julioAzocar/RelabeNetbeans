    public void Cargar() {
        String strMsg = "";

        try {

            //llenar combo tipo
            CParametro objt = new CParametro(0, "", 0, "", 0);
            objt.LLenarParametro();
            String strDato = "";

            this.cmbparametro.addItem("");
            for (int x = 0; x < objt.strDatosCombo.length; x++) {
                strDato = objt.strDatosCombo[x];
                cmbparametro.addItem(strDato);
            }

        } catch (Exception ex) {
            strMsg = ex.getMessage();
            //JOptionPane.showMessageDialog(null, strMsg);
        }
    }

