    private void cmbparametroItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbparametroItemStateChanged
        // TODO add your handling code here:

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String parametro = cmbparametro.getSelectedItem().toString();
            if (parametro.equals("") == true) {
                //JOptionPane.showMessageDialog(null, "Ingrese tipo de parametro");
                return;
            } else {
                parametro = parametro.substring(0, parametro.indexOf(" "));
                this.jTextAreaEdit.setText(this.jTextAreaEdit.getText() + "PR" + parametro);
            }
        }

    }//GEN-LAST:event_cmbparametroItemStateChanged

